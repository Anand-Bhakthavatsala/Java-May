package JavaPractice;

public class SwitchExample {

	public static void main(String[] args) {
	
		int month =2;
		
		switch (month)
		{
		case 1:
			System.out.println("We are in Jan month");
			break;
		case 2:
			System.out.println("We are in Feb month");
			break;
		case 5:
			System.out.println("We are in May month");
			break;
		case 12:
			System.out.println("We are in Dec month");
			break;
		}		

	}

}
